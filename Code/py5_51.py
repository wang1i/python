import turtle as t
t.pencolor("yellow")
t.pensize(20)
t.penup()
t.goto(-150, 100)
t.pendown()
t.sety(-100)
t.setx(-200)
t.sety(0)

t.penup()
t.goto(0, 0)
t.pendown()
t.circle(50)
t.penup()
t.goto(0, -100)
t.pendown()
t.circle(50)

t.penup()
t.goto(130, 100)
t.pendown()
t.sety(-100)
t.setx(230)
t.sety(100)
t.penup()
t.goto(90, 50)
t.pendown()
t.setx(270)
t.penup()
t.goto(130, -20)
t.pendown()
t.setx(230)

t.hideturtle()
t.done()